CREATE DATABASE FACTURACION_ANALISIS;
USE FACTURACION_ANALISIS;

/* ################### DEFINICION DEL LOGIN ################### */
/*
	ROL
		Admin 
		Vendedor
*/
CREATE TABLE ROL(
	ID INT NOT NULL PRIMARY KEY,
	NOMBRE VARCHAR(30)
);

CREATE TABLE USUARIOS(
	ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	NOMBRE VARCHAR(50),
	APELLIDOS VARCHAR(30),
	NICK_NAME VARCHAR(30),
	PASSWORD VARCHAR(30),
	EMAIL VARCHAR(50)
);

/* ################### CONFIGURACION DEL SISTEMA ################### */
CREATE TABLE CONFIGURACION(
	ID INT NOT NULL PRIMARY KEY,
	NOMBRE_COMERCIAL VARCHAR(50),
	RAZON_SOCIAL VARCHAR(50),
	DIRECCION VARCHAR(100),
	TELEFONO VARCHAR(12),
	NIT VARCHAR(12),
	EMAIL VARCHAR(50)
);

/* ################### RESOLUCIONES ################### */
CREATE TABLE RESOLUCION(
	ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	NO_RESOLUCION VARCHAR(30) NOT NULL,
	NO_SERIE VARCHAR(30) NOT NULL,
	NO_INICIAL INT NOT NULL,
	NO_FINAL INT NOT NULL,
	NO_ACTUAL INT NOT NULL,
	FECHA_AUTORIZACION DATE NOT NULL,
);

/* ################### CLIENTES ################### */
CREATE TABLE CLIENTE(
	ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	NOMBRE VARCHAR(50),
	APELLIDOS VARCHAR(30),
	NIT VARCHAR(12),
	DIRECCION VARCHAR(100),
	TELEFONO VARCHAR(12),
	EMAIL VARCHAR(50)
);

/* ################### PRODCUTO ################### */
CREATE TABLE CATEGORIA(
	ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	NOMBRE VARCHAR(30) NOT NULL
);

CREATE TABLE PRODUCTO(
	ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	
);



