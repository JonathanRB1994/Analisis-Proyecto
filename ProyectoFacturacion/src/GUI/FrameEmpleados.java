    private void btEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEstadoActionPerformed
        try {
            DefaultTableModel tm = (DefaultTableModel) tableEmpleados.getModel();
            String id=String.valueOf(tm.getValueAt(tableEmpleados.getSelectedRow(),0));

            if(habilitar){
                if(logica.changeStateEmpleado(id, "1")){
                    JOptionPane.showMessageDialog(this, "Empleado "+id+" habilitado.", "Correcto", JOptionPane.PLAIN_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "El empleado "+id+" no se puedo habilitar.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                if(logica.changeStateEmpleado(id, "0")){
                    JOptionPane.showMessageDialog(this, "Empleado "+id+" deshabilitado.", "Correcto", JOptionPane.PLAIN_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "El empleado "+id+" no se puedo deshabilitado.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            actualizar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Selecciones una fila de la tabla.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btEstadoActionPerformed

    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        String nombre="";
        String apellidos="";
        nombre+=txNombre.getText().toUpperCase();
        apellidos+=txApellidos.getText().toUpperCase();

        boolean datos=false;
        String condiciones="";
        if(rbSI.isSelected()){
            condiciones+=" AND E.ESTADO=1 ";
        }else{
            condiciones+=" AND E.ESTADO=0 ";
        }
        String mensaje="Se realizo una busqueda por ";
        if(!nombre.equals("")){
            datos=true;
            condiciones+=" AND E.NOMBRE LIKE \'%"+nombre.toUpperCase()+"%\' ";
            mensaje+="NOMBRE";
        }
        if(!apellidos.equals("")){
            datos=true;
            condiciones+=" AND E.APELLIDOS LIKE \'%"+apellidos.toUpperCase()+"%\' ";
            mensaje+=" APELIIDOS";
        }
        mensaje+=".";
        
        if(!datos){
            actualizar();
            return;
        }

        JOptionPane.showMessageDialog(this, mensaje, "Buscado", JOptionPane.PLAIN_MESSAGE);
        DefaultTableModel modelo = logica.getModelEmpleados(condiciones);
        TableRowSorter sorter = new TableRowSorter(modelo);
        tableEmpleados.setModel(modelo);
        tableEmpleados.setRowSorter(sorter);
    }//GEN-LAST:event_btBuscarActionPerformed

