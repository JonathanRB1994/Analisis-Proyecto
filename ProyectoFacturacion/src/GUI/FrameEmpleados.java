    private void actualizar(){
        String condiciones="";
        if(rbSI.isSelected()){
            condiciones+=" AND ESTADO=1 ";
        }else{
            condiciones+=" AND ESTADO=0 ";
        }
        DefaultTableModel modelo = logica.getModelEmpleados(condiciones);
        TableRowSorter sorter = new TableRowSorter(modelo);
        tableEmpleados.setModel(modelo);
        tableEmpleados.setRowSorter(sorter);
        
        txNombre.setText("");
        txApellidos.setText("");
        
        empleados=logica.getListEmpleados();
        
        autoNombre = new TextAutoCompleter(txNombre);
        autoApellidos = new TextAutoCompleter(txApellidos);
        
        autoNombre.setCaseSensitive(false);
        autoApellidos.setCaseSensitive(false);
        
        autoNombre.setMode(0);
        autoApellidos.setMode(0);

        
        for (int i = 0; i < empleados.size(); i++) {
            autoNombre.addItem(empleados.get(i).getNombre());
            autoApellidos.addItem(empleados.get(i).getApellidos());
        }
        
    }
