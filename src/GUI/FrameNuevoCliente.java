    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        if( txNombre.getText().equals("") || txApellidos.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Los campos Nombre y Apellidos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }        
        
        if( logica.insertCliente(new Cliente( txNombre.getText().toUpperCase(), txApellidos.getText().toUpperCase(), txTelefono.getText(),
                (txDireccion.getText().equals(""))? "CIUDAD":txDireccion.getText().toUpperCase(), txEmail.getText().toUpperCase(), 
                (txNIT.getText().equals("")? "C/F":txNIT.getText().toUpperCase()) ) )){
            JOptionPane.showMessageDialog(this, "Cliente agregado.", "Correcto", JOptionPane.PLAIN_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "No se puedo agregar el cliente.", "Error", JOptionPane.ERROR_MESSAGE);                   
        }
    }//GEN-LAST:event_btGuardarActionPerformed

